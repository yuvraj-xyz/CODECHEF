#include <bits/stdc++.h>
using namespace std;

// Is it hot or cold - HOTCOLD
int main()
{
    int t;
    cin >> t;

    while(t--)
    {
        int c;
        cin >> c;

        if(c>20)
        {
            cout << "HOT" << endl;
        }
        else
        {
            cout << "COLD"  << endl;
        }
    }
}
